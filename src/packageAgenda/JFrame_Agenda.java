    private void jMenuItem_ImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_ImportActionPerformed
        JFileChooser fileChooser = new JFileChooser();

        if (fileChooser.showOpenDialog(JFrame_Agenda.this) == JFileChooser.APPROVE_OPTION) {
            ContactFile imp = new ContactFile(fileChooser.getSelectedFile());
            System.out.println("A creat fisierul");
            List<Contact> lines = imp.importFromFile();
            System.out.println("A creat lista");
            System.out.println(lines.toString());
            lines.forEach((Contact impC) -> {
                try {
                    if (checkIfDuplicateAtImport(impC.toString()) == true) {
                        System.out.println("This is a duplicate contact");
                    } else {
                        contactFile.adauga(impC);
                    }
                } catch (IOException ex) {
                    System.out.println(ex.getMessage());
                }
            });
        }
        actualizeazaModel();
    }//GEN-LAST:event_jMenuItem_ImportActionPerformed

    private void jMenuItem_ExportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_ExportActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        if (fileChooser.showOpenDialog(JFrame_Agenda.this) == JFileChooser.APPROVE_OPTION) {
            actualizeazaModel();
            ContactFile exp = new ContactFile(fileChooser.getSelectedFile());

            if (fileChooser.getSelectedFile().toPath().toString().endsWith(".txt")) {
                for (int i = 0; i <= jList_Contacte.getModel().getSize() - 1; i++) {
                    Object value = jList_Contacte.getModel().getElementAt(i);
                    try {
                        exp.adauga((Contact) value);
                    } catch (IOException ex) {
                        Logger.getLogger(JFrame_Agenda.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Trebuie sa alegeti un fisier \".txt\"");
            }
        }
        actualizeazaModel();
    }//GEN-LAST:event_jMenuItem_ExportActionPerformed

