    private void reloadList(String fil, String srt) {
        model.clear();
        jTextField_Filtru.setEnabled(false);
        jButton_Filter.setEnabled(false);
        clearAll();
        if (fil.equals("numere de MOBIL")) {
            contactFile.getMobileContacts(srt).forEach((e) -> model.addElement(e));
        } else if (fil.equals("numere de FIX")) {
            contactFile.getFixContacts(srt).forEach((e) -> model.addElement(e));
        } else if (fil.equals("aniversati astazi")) {
            contactFile.getTodayAniversaryContacts(srt).forEach((e) -> model.addElement(e));
        } else if (fil.equals("aniversati in curand")) {
            contactFile.getSoonToBeAniversaryContacts(srt).forEach((e) -> model.addElement(e));
        } else if (fil.equals("Personalizata")) {
            jTextField_Filtru.setEnabled(true);
            jButton_Filter.setEnabled(true);
            contactFile.getSomeContacts(jTextField_Filtru.getText(), srt).forEach((e) -> model.addElement(e));
        }
    }

