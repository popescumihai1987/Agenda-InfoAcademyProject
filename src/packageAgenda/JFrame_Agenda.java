    private void runBanner() throws InterruptedException {
        File dir = new File(".");
        File[] bannereJpg = dir.listFiles(f -> f.getName().endsWith(".jpg"));

        TimerTask task;
        task = new TimerTask() {
            @Override
            public void run() {
                randomNum = rand.nextInt(4);
                ImageIcon poza = new ImageIcon(bannereJpg[randomNum].getAbsolutePath());
                jLabel_Banner.setIcon(poza);
                jLabel_Banner.setText("");
                if (isRegistered()) {
                    jLabel_Banner.setIcon(null);
//                    jLabel_Banner.setVisible(false);
//                    jLabel_Banner.setOpaque(true);
                    jLabel_Banner.setForeground(Color.BLACK);
                    jLabel_Banner.setText("REGISTERED");
                    jLabel_registermsg.setVisible(false);
//                    jLabel_Banner.setBackground(Color.CYAN);
                    this.cancel();
                }
            }
        };
        Timer timer = new Timer("Timer");
        timer.scheduleAtFixedRate(task, 0, 1000);
        Thread.sleep(1000);
    }


    private void checkIfDuplicate() {
        for (int i = 0; i <= jList_Contacte.getModel().getSize() - 1; i++) {
            Object value = jList_Contacte.getModel().getElementAt(i);
            String[] values = value.toString().split(",");

            if (jTextField_TEL.getText().equals(values[2]) &&
                    jTextField_Nume.getText().equals(values[0]) &&
                    jTextField_Prenume.getText().equals(values[1]) &&
                    jTextFieldDataNasterii.getText().equals(values[3])) {
                throw new UnsupportedOperationException("You already have this contact in your list!");
            }

        }
    }

    private boolean checkIfDuplicateAtImport(String row) {
        for (int i = 0; i <= jList_Contacte.getModel().getSize() - 1; i++) {
            Object value = jList_Contacte.getModel().getElementAt(i);
            String[] values = value.toString().split(",");
            String[] importedValues = row.toString().split(",");

            if (importedValues[2].equals(values[2]) &&
                    importedValues[0].equals(values[0]) &&
                    importedValues[1].equals(values[1])) {
                return true;
            }
        }
        return false;
    }

